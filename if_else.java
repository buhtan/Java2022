package degişkenler;

public class if_else {

	public static void main(String[] args) {
		int sayi =24;
		if(sayi<20) {
			System.out.println("Sayı 20'den küçüktür");
		}else if(sayi==20) {
			System.out.println("Sayı 20' eşittir");
		}else {
			System.out.println("Sayı 20'den büyüktür");
		}

	}

}
